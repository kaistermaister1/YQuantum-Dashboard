def test_int_comparator_two_unknowns(num_bits=3, sign=">", verbose=True):
    """
    Test function for IntegerComparatorTwoUnknowns.

    The goal is to embed the inequality x [sign] y in a circuit of linear equations.

    Here [sign] can be ">" or "<=".

    Both x and y are left as unknown. The only input is the number of bits.
    """

    assert sign in [">", "<="]

    num_carry_bits = num_bits
    num_z_bits = num_bits + 1
    num_aux_bits = 3 * (num_bits - 1)

    vm = VariableManager()
    x_bits = [vm.new_variable(f"x_{i}_") for i in range(num_bits)]
    y_bits = [vm.new_variable(f"y_{i}_") for i in range(num_bits)]
    y_comp_bits = [vm.new_variable(f"y_comp_{i}_") for i in range(num_bits)]
    carry_bits = [vm.new_variable(f"c_{i}_") for i in range(num_carry_bits)]
    z_bits = [vm.new_variable(f"z_{i}_") for i in range(num_z_bits)]
    aux_bits = [vm.new_variable(f"aux_{i}_") for i in range(num_aux_bits)]

    int_comparator = IntegerComparatorTwoUnknowns(
        x_bits,
        y_bits,
        y_comp_bits,
        carry_bits,
        z_bits,
        aux_bits,
        sign,
    )
    max_satisfied = int_comparator.max_num_satisfied
    count_more_than_max = 0  # number of assignments that satisfy more equations than maximum number of possible equations satisfied

    # The overall approach is to iterate through all possible values of x and y integers of the specified number of bits.
    # For each combination of x and y assignment, we also iterate through the possible assignments for the other bits.

    if verbose:
        print("")
        print(
            "Integer comparator with two unknowns. Every maximally satisfying assignment matches the comparator logic: x "
            + sign
            + " y",
        )
        print("")
        print(
            " ".join(f"x{i}" for i in range(num_bits))
            + " ".join(f"y{i}" for i in range(num_bits))
            + " ".join(f"-y{i}" for i in range(num_bits))
            + "| #  | c?   | z?   | y_comp? | x comp y?",
        )
        print("----------------------------------------------------------------------")

    # compile a dictionary of correct bit assignments
    correct_assignments = []
    for values in product([0, 1], repeat=3 * num_bits):
        x_value_int = sum([2**i * values[i] for i in range(num_bits)])
        y_value_int = sum([2**i * values[i + num_bits] for i in range(num_bits)])
        y_bits_ = values[num_bits : 2 * num_bits]
        y_comp_bits_ = values[2 * num_bits :]

        if sign == ">":
            if x_value_int > y_value_int and all(
                [
                    True
                    if (y_bits_[i] == 1 and y_comp_bits_[i] == 0)
                    or (y_bits_[i] == 0 and y_comp_bits_[i] == 1)
                    else False
                    for i in range(num_bits)
                ],
            ):
                correct_assignments.append(values)
        if sign == "<=":
            if x_value_int <= y_value_int and all(
                [
                    True
                    if (y_bits_[i] == 1 and y_comp_bits_[i] == 0)
                    or (y_bits_[i] == 0 and y_comp_bits_[i] == 1)
                    else False
                    for i in range(num_bits)
                ],
            ):
                correct_assignments.append(values)

    for values in product([0, 1], repeat=3 * num_bits):
        x_bits_assigned = values[0:num_bits]
        y_bits_assigned = values[num_bits : 2 * num_bits]
        y_comp_bits_assigned = values[2 * num_bits :]
        x_value_int = sum([x_bits_assigned[i] * (2**i) for i in range(num_bits)])
        y_value_int = sum([y_bits_assigned[i] * (2**i) for i in range(num_bits)])

        # compute the expected values of y_comp, carry, and z bits
        y_comp_bits_expected = [0 if y == 1 else 1 for y in y_bits_assigned]
        carry_bits_expected = [0] * num_bits
        z_bits_expected = [0] * (num_bits + 1)
        for i in range(num_bits):
            if i == 0:
                z_bits_expected[i] = (x_bits_assigned[i] + y_comp_bits_expected[i]) % 2
                carry_bits_expected[i] = x_bits_assigned[i] * y_comp_bits_expected[i]
            else:
                z_bits_expected[i] = (
                    x_bits_assigned[i]
                    + y_comp_bits_expected[i]
                    + carry_bits_expected[i - 1]
                ) % 2
                carry_bits_expected[i] = (
                    x_bits_assigned[i] * y_comp_bits_expected[i]
                    + x_bits_assigned[i] * carry_bits_expected[i - 1]
                    + y_comp_bits_expected[i] * carry_bits_expected[i - 1]
                ) % 2
        z_bits_expected[num_bits] = carry_bits_expected[num_bits - 1]

        # check the assignment of x, y, and y_comp bits
        if sign == ">":
            x_compare_y = x_value_int > y_value_int
        if sign == "<=":
            x_compare_y = x_value_int <= y_value_int
        y_comp_bits_correct = [
            y_comp_bits_assigned[i] == y_comp_bits_expected[i] for i in range(num_bits)
        ]

        # check how many equations are satisfied
        max_print = (
            5  # maximum number of lines (combination of bit assignments) printed
        )
        print_count = 0  # counter for the lines printed

        num_additional_bits = num_carry_bits + num_z_bits + num_aux_bits
        for additional_values in product([0, 1], repeat=num_additional_bits):

            carry_bits_assigned = additional_values[:num_carry_bits]
            z_bits_assigned = additional_values[
                num_carry_bits : num_carry_bits + num_z_bits
            ]
            aux_bits_assigned = additional_values[num_carry_bits + num_z_bits :]

            # Create a dictionary for the assignment.
            assignment = {x_bits[i]: x_bits_assigned[i] for i in range(num_bits)}
            assignment.update({y_bits[i]: y_bits_assigned[i] for i in range(num_bits)})
            assignment.update(
                {y_comp_bits[i]: y_comp_bits_assigned[i] for i in range(num_bits)},
            )
            assignment.update(
                {carry_bits[i]: carry_bits_assigned[i] for i in range(num_carry_bits)},
            )
            assignment.update(
                {z_bits[i]: z_bits_assigned[i] for i in range(num_z_bits)},
            )
            assignment.update(
                {aux_bits[i]: aux_bits_assigned[i] for i in range(num_aux_bits)},
            )

            # Check if the expected sum and carry bits are correct.
            carry_bits_correct = [
                assignment[carry_bits[i]] == carry_bits_expected[i]
                for i in range(num_carry_bits)
            ]
            z_bits_correct = [
                assignment[z_bits[i]] == z_bits_expected[i] for i in range(num_z_bits)
            ]

            # Evaluate the equations.
            eq_evals = int_comparator.evaluate(assignment)
            total_satisfied = sum(eq_evals)

            if total_satisfied >= max_satisfied:

                if total_satisfied > max_satisfied:
                    count_more_than_max = count_more_than_max + 1

                if verbose and print_count < max_print:
                    print_count = print_count + 1
                    print(
                        "  ".join(map(str, values))
                        + f"  | {total_satisfied}"
                        + f" | {all(carry_bits_correct)}"
                        + f" | {all(z_bits_correct)}"
                        + f" | {all(y_comp_bits_correct)}"
                        + f"  | {x_compare_y}",
                    )

                if (
                    values in correct_assignments
                ):  # remove the assignment from the set of correct assignments
                    correct_assignments.remove(values)

                if all(carry_bits_correct) == False or all(z_bits_correct) == False:
                    print("False assignment detected. Details:")
                    print(assignment)
                    int_comparator.evaluate(assignment, verbose=True)
                    if all(carry_bits_correct) == False:
                        print("carry bits evaluation: " + str(carry_bits_correct))
                        for i, result in enumerate(carry_bits_correct):
                            if result == False:
                                print(
                                    "index = "
                                    + str(i)
                                    + ", assigned: "
                                    + str(assignment[carry_bits[i]])
                                    + ", expected: "
                                    + str(carry_bits_expected[i]),
                                )
                    if all(z_bits_correct) == False:
                        print("z bits evaluation:  " + str(z_bits_correct))
                        for i, result in enumerate(z_bits_correct):
                            if result == False:
                                print(
                                    "index = "
                                    + str(i)
                                    + ", assigned: "
                                    + str(assignment[z_bits[i]])
                                    + ", expected: "
                                    + str(z_bits_expected[i]),
                                )
                    print("")

    if verbose:
        print("")
        print("Integer comparator with two unknowns.")
        print("")
        if count_more_than_max > 0:
            print(
                "Total number of assignments with more than "
                + str(max_satisfied)
                + " equations satisfied: "
                + f"{count_more_than_max}"
                + ".",
            )
        else:
            print(
                "No assignment has more than "
                + str(max_satisfied)
                + " equations satisfied.",
            )
        print("")

    # Check if all correct assignments are discovered as maximally satisfying solutions
    if len(correct_assignments) == 0:
        if verbose:
            print("-----------------------------------------------------")
            print("All correct assignments are discovered.")
            print("-----------------------------------------------------")
            print("")
        return True
    else:
        if verbose:
            print("-----------------------------------------------------")
            print(
                "Not all correct assignments are covered by the set of maximally satisfying solutions.",
            )
            print("-----------------------------------------------------")
            print("Remaining assignments not covered:")
            print(correct_assignments)
        return False


def test_int_equality(y=20, verbose=True):
    """
    Test function for IntegerEqualityOneUnknown.

    The goal is to embed the inequality x = y in a circuit of linear equations.

    y is provided as an given input.
    """

    num_bits = len(bin(y)[2:])
    y_bits = [int(bit) for bit in bin(y)[2:]][::-1] + [0] * (num_bits - y.bit_length())

    vm = VariableManager()
    x_bits = [vm.new_variable(f"x_{i}_") for i in range(num_bits)]

    int_equality = IntegerEqualityOneUnknown(x_bits, y_bits)

    # The overall approach is to iterate over all possible combinations of the x bits.
    # For each combination, we compute the expected carry and z bits.
    # Then we check how many equations are satisfied.

    if verbose:
        print("")
        print(
            "Integer equality. Every maximally satisfying assignment matches the equality logic: x = y",
        )
        print("")
        print(
            " ".join(f"x{i}" for i in range(num_bits))
            + " | #  | x  | x="
            + str(y)
            + "?",
        )
        print("-----------------------------------------------------")

    # Compile a dictionary of correct bit assignments
    correct_assignments = []
    for values in product([0, 1], repeat=num_bits):
        x_value_int = sum([2**i * values[i] for i in range(num_bits)])
        if x_value_int == y:
            correct_assignments.append(values)

    for values in product([0, 1], repeat=num_bits):
        x_bits_assigned = values
        x_value_int = sum(2**i * values[i] for i in range(num_bits))

        # check how many equations are satisfied
        assignment = {x_bits[i]: x_bits_assigned[i] for i in range(num_bits)}

        # Evaluate the equations.
        eq_evals = int_equality.evaluate(assignment)
        total_satisfied = sum(eq_evals)
        max_satisfied = int_equality.max_num_satisfied

        if total_satisfied == max_satisfied:
            if total_satisfied > max_satisfied:
                print(
                    "Error: more than " + str(max_satisfied) + " equations satisfied.",
                )
            if verbose:
                print(
                    "  ".join(map(str, values))
                    + f"  | {total_satisfied}"
                    + f" | {x_value_int}"
                    + f"  | {x_value_int == y}",
                )

            if values in correct_assignments:
                correct_assignments.remove(values)
            if x_value_int != y:
                print("False assignment detected. Details:")
                print(assignment)
                int_equality.evaluate(assignment, verbose=True)
                print("")

    # Check if all correct assignments are discovered as maximally satisfying solutions
    if len(correct_assignments) == 0:
        if verbose:
            print("-----------------------------------------------------")
            print("All correct assignments are discovered.")
            print("-----------------------------------------------------")
            print("")
        return True
    else:
        if verbose:
            print("-----------------------------------------------------")
            print(
                "Not all correct assignments are covered by the set of maximally satisfying solutions.",
            )
            print("-----------------------------------------------------")
            print("")
        return False


