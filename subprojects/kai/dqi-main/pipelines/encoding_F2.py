class ANDGadget(Gadget):
    """
    Gadget for Boolean AND.
    Given inputs x and y and output z, the constraints ensure that z = x AND y.

    The typical formulation uses:
      1) x + y + z = 1  (mod 2)
      2) x + z = 0      (mod 2)
      3) y + z = 0      (mod 2)
      4) z = 0          (mod 2)
    """

    def __init__(self, x, y, z, mod=2):
        super().__init__(mod)
        self.input_vars = [x, y]
        self.output_vars = [z]
        self.all_vars = [x, y, z]
        self.generate_constraints()

    def generate_constraints(self):
        # Equation: x + y + z = 1  (mod 2)
        self.equations.append(
            Equation(
                {
                    self.input_vars[0]: 1,
                    self.input_vars[1]: 1,
                    self.output_vars[0]: 1,
                },
                1,
                self.mod,
            ),
        )
        # Equation: x + z = 0  (mod 2)
        self.equations.append(
            Equation(
                {
                    self.input_vars[0]: 1,
                    self.output_vars[0]: 1,
                },
                0,
                self.mod,
            ),
        )
        # Equation: y + z = 0  (mod 2)
        self.equations.append(
            Equation(
                {
                    self.input_vars[1]: 1,
                    self.output_vars[0]: 1,
                },
                0,
                self.mod,
            ),
        )

        # Equation: z = 0 (mod 2)
        self.equations.append(Equation({self.output_vars[0]: 1}, 0, self.mod))

        self.max_num_satisfied = 3  # only 3 equations are satisfied


