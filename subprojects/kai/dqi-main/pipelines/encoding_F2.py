#####################################
# Equation Class with Integer Coeff #
#####################################


class Equation:
    """
    Represents a linear equation in modular arithmetic.
    For example, for a mod-2 equation:
         c1*x1 + c2*x2 + ... = rhs   (mod 2)
    where the coefficients can be arbitrary integers.

    On initialization, each coefficient and the right-hand side is reduced modulo `mod`
    and terms with a zero coefficient (after reduction) are omitted.
    """

    def __init__(self, coefficients, rhs, mod=2):
        self.mod = mod
        # Reduce each coefficient modulo mod and keep nonzero terms.
        self.coefficients = {
            var: coeff % mod
            for var, coeff in coefficients.items()
            if (coeff % mod) != 0
        }
        self.rhs = rhs % mod

    def is_satisfied(self, assignment):
        """
        Evaluate the equation for a given assignment (dict mapping variable -> integer value).
        Returns True if the equation holds modulo `mod`.
        """
        total = 0
        for var, coeff in self.coefficients.items():
            total += coeff * assignment.get(var, 0)
        return (total % self.mod) == self.rhs

    def __repr__(self):
        if not self.coefficients:
            terms = "0"
        else:
            terms = " + ".join(
                f"{coeff}*{var}" for var, coeff in self.coefficients.items()
            )
        return f"{terms} = {self.rhs} (mod {self.mod})"


##############################
# Gadget Base Class          #
##############################


class Gadget(ABC):
    """
    Abstract base class for all gadgets.
    A gadget converts a (possibly non-linear) Boolean constraint into a set of modular equations.
    """

    def __init__(self, mod=2):
        self.equations = []  # List of Equation objects generated by this gadget.
        self.input_vars = []  # Variables provided as input.
        self.aux_vars = []  # Auxiliary variables created internally.
        self.output_vars = []  # Variables that represent the gadget's output.
        self.mod = (
            mod  # The size of the finite field that the equations are defined on.
        )
        self.max_num_satisfied = 0  # The maximum number of satisfied equations.

    @abstractmethod
    def generate_constraints(self):
        """
        Generate modular constraints for the gadget.
        The constraints should be stored in self.equations.
        """
        pass

    def evaluate(self, assignment, verbose=False):
        """
        Evaluate all constraints for a given assignment.
        Returns the number of constraints satisfied.
        """

        results = [eq.is_satisfied(assignment) for eq in self.equations]

        if verbose:
            for eq, result in zip(self.equations, results):
                print(f"{eq} -> {result}")

        return results


