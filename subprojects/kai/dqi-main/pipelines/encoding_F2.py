class CARRYGadget(Gadget):
    """
    Gadget for the carry operation in binary addition.
    General formulation:
        x + y + carry_in = 2*carry_out + sum_bit
    This reflects the schoolbook method for addition.

    The output carry bit is computed as:
        carry_out = (x * carry_in) + (y * carry_in) + (x * y)
    where each term is computed using AND gadgets.

    Let a = x * carry_in, b = y + carry_in, and c = x * y.
    Then the carry_out can be computed as:
        carry_out = a + b + c
    where a, b, c are computed using AND gadgets.

    The constraints are:
        1) carry_out = a + b + c (mod 2)
        2) a = x * carry_in (AND gadget)
        3) b = y * carry_in (AND gadget)
        4) c = x * y (AND gadget)
        5) x + y + carry_in = sum_bit (mod 2)
    """

    def __init__(self, x, y, carry_in, carry_out, sum_bit, a, b, c, mod=2):
        super().__init__(mod)
        self.input_vars = [x, y, carry_in]
        self.aux_vars = [a, b, c]
        self.output_vars = [sum_bit, carry_out]
        self.all_vars = [x, y, carry_in, sum_bit, carry_out]
        self.generate_constraints()

    def generate_constraints(self):

        # 1) carry_out = a + b + c (mod 2)
        self.equations.append(
            Equation(
                {
                    self.output_vars[1]: 1,
                    self.aux_vars[0]: 1,
                    self.aux_vars[1]: 1,
                    self.aux_vars[2]: 1,
                },
                0,
                self.mod,
            ),
        )

        # 2) a = x * carry_in (AND gadget)
        ANDGadget_a = ANDGadget(
            self.input_vars[0],
            self.input_vars[2],
            self.aux_vars[0],
        )
        self.equations = self.equations + ANDGadget_a.equations

        # 3) b = y * carry_in (AND gadget)
        ANDGadget_b = ANDGadget(
            self.input_vars[1],
            self.input_vars[2],
            self.aux_vars[1],
        )
        self.equations = self.equations + ANDGadget_b.equations

        # 4) c = x * y (AND gadget)
        ANDGadget_c = ANDGadget(
            self.input_vars[0],
            self.input_vars[1],
            self.aux_vars[2],
        )
        self.equations = self.equations + ANDGadget_c.equations

        # 5) x + y + carry_in = sum_bit (mod 2)
        self.equations.append(
            Equation(
                {
                    self.input_vars[0]: 1,
                    self.input_vars[1]: 1,
                    self.input_vars[2]: 1,
                    self.output_vars[0]: 1,
                },
                0,
                self.mod,
            ),
        )

        self.max_num_satisfied = 11  # at most 11 equations can be satisfied out of the equations mod 2 for CARRYGadget


class CARRYOneGadget(Gadget):
    """
    CARRY Gadget for a special case of the schoolbook addition.
    General formulation:
        x + y = 2*carry_out + sum_bit
    This reflects the schoolbook method for addition.
    The output carry bit is computed as:
        carry_out = (x * y)
    which term is computed using AND gadgets.
    The output sum bit is computed as:
        sum_bit = x + y
    The constraints are:
        1) carry_out = x * y (AND gadget)
        2) x + y = sum_bit (mod 2)
    """

    def __init__(self, x, y, carry_out, sum_bit, mod=2):
        super().__init__(mod)
        self.input_vars = [x, y]
        self.output_vars = [sum_bit, carry_out]
        self.all_vars = [x, y, sum_bit, carry_out]
        self.generate_constraints()

    def generate_constraints(self):

        # 1) carry_out = x * y (AND gadget)
        ANDGadget_carry_out = ANDGadget(
            self.input_vars[0],
            self.input_vars[1],
            self.output_vars[1],
        )
        self.equations = self.equations + ANDGadget_carry_out.equations

        # 2) x + y = sum_bit (mod 2)
        self.equations.append(
            Equation(
                {
                    self.input_vars[0]: 1,
                    self.input_vars[1]: 1,
                    self.output_vars[0]: 1,
                },
                0,
                self.mod,
            ),
        )

        self.max_num_satisfied = 4  # maximum number of equations that can be satisfied


class CARRYTwoGadget(Gadget):
    """
    CARRY Gadget for another special case of the schoolbook addition.
    General formulation:
        x + 1 + carry_in = 2*carry_out + sum_bit
    This reflects the schoolbook method for addition.
    The output carry bit is computed as:
        carry_out = x + carry_in + x*carry_in
    which term is computed using AND gadgets.
    The output sum bit is computed as:
        sum_bit = x + 1 + carry_in
    The constraints are:
        1) carry_out = x + carry_in + x*carry_in (with AND gadget)
        2) x + 1 + carry_in = sum_bit (mod 2)
    """

    def __init__(self, x, carry_in, carry_out, sum_bit, mod=2):
        super().__init__(mod)
        self.input_vars = [x, carry_in]
        self.output_vars = [sum_bit, carry_out]
        self.all_vars = [x, carry_in, sum_bit, carry_out]
        self.generate_constraints()

    def generate_constraints(self):

        # 1) carry_out = x + carry_in + x*carry_in implemented with the following equations:
        ## carry_out = 1
        self.equations.append(Equation({self.output_vars[1]: 1}, 1, self.mod))
        ## carry_out + carry_in = 0
        self.equations.append(
            Equation(
                {
                    self.output_vars[1]: 1,
                    self.input_vars[1]: 1,
                },
                0,
                self.mod,
            ),
        )
        ## x + carry_out = 0
        self.equations.append(
            Equation(
                {
                    self.input_vars[0]: 1,
                    self.output_vars[1]: 1,
                },
                0,
                self.mod,
            ),
        )
        ## x + carry_in + carry_out = 0
        self.equations.append(
            Equation(
                {
                    self.input_vars[0]: 1,
                    self.input_vars[1]: 1,
                    self.output_vars[1]: 1,
                },
                0,
                self.mod,
            ),
        )

        # 2) x + 1 + carry_in = sum_bit (mod 2)
        self.equations.append(
            Equation(
                {
                    self.input_vars[0]: 1,
                    self.input_vars[1]: 1,
                    self.output_vars[0]: 1,
                },
                1,
                self.mod,
            ),
        )

        self.max_num_satisfied = 4  # maximum number of equations satisfied


