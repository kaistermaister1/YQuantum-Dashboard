def test_multiple_integer_adder(a=[2, 1, 0, 3], verbose=True):
    """
    Function for testing MultipleIntegerAdder.

    Input:
    a : List[Int]
        List of integer coefficients in the constraint.
    NOTE: testing time becomes prohibitively long for len(a) > 3.
    """

    tree = MultipleIntegerAdder.build_tree_structure(a)
    precomputed_tree = MultipleIntegerAdder.calculate_num_bits(a, tree)

    # Calculate the total number of various types of bits
    total_carry_bits = 0
    total_aux_bits = 0
    total_intermediate_bits = 0
    total_output_bits = 0
    nonzero_indices = []
    for node_name, node_data in precomputed_tree.items():
        if "ind_term" in node_data:
            nonzero_indices.append(node_data["ind_term"])
        if "n_carry_bits" in node_data:
            total_carry_bits += node_data["n_carry_bits"]
        if "n_aux_bits" in node_data:
            total_aux_bits += node_data["n_aux_bits"]
        if "n_intermediate_bits" in node_data:
            total_intermediate_bits += node_data["n_intermediate_bits"]
        if "n_output_bits" in node_data:
            total_output_bits += node_data["n_output_bits"]
    m = len(nonzero_indices)  # total number of (non-zero) terms
    a_ = [a[i] for i in nonzero_indices]

    # Introduce the variables
    vm = VariableManager()
    x_vars = [vm.new_variable(f"x_{i}_") for i in range(0, len(a))]
    carry_bits = [vm.new_variable(f"c_{i}_") for i in range(total_carry_bits)]
    aux_bits = [vm.new_variable(f"aux_{i}_") for i in range(total_aux_bits)]
    intermediate_bits = [
        vm.new_variable(f"int_{i}_") for i in range(total_intermediate_bits)
    ]
    y_vars = [vm.new_variable(f"y_{i}_") for i in range(total_output_bits)]

    miadder = MultipleIntegerAdder(
        x_vars,
        a,
        carry_bits,
        intermediate_bits,
        y_vars,
        aux_bits,
        precomputed_tree,
    )

    # The overall approach is to start with different input values x1, x2, ..., xm, of which there will be in total 2^(m) combinations.
    # For each combination, we compute the expected sum and carry bits and check how many equations are satisfied.
    if verbose:
        print("")
        print(
            "Multiple Integer Adder. Every maximally satisfying assignment matches the addition logic:",
        )
        print(
            "In total "
            + f"{len(miadder.equations)}"
            + " equations on "
            + f"{str(vm.current)}"
            + " variables are generated.",
        )
        print(
            "To limit the output, only assignments of the input bits x1, x2, ..., xm, and the output y bits are printed.",
        )
        print("The correctness of intermediate bits (``int?``) is also checked.")
        print(
            "The possible combinations of other bits (auxiliary, output b, and carry) are iterated over as well but not printed.",
        )
        print("")
        print(
            ", ".join([f"x{i}" for i in nonzero_indices])
            + ", "
            + ", ".join([f"y{i}" for i in range(total_output_bits)])
            + "  | #  | int? | out?",
        )
        print("-----------------------------------------------------")

    # Compile a dictionary of correct bit assignments.
    correct_assignments = []
    for values in product([0, 1], repeat=m + total_output_bits):
        x_assigned = values[:m]
        y_assigned = values[m:]
        y_value_int = sum([2**i * y_assigned[i] for i in range(total_output_bits)])
        if (
            sum([a_[i] * x_assigned[i] for i in range(0, m)]) == y_value_int
        ):  # if a1*x1 + a2*x2 + ... + am*xm = y
            correct_assignments.append(list(values))

    # Iterate over all combinations of x1, x2, ..., xm, and y bits.
    num_group_1 = (
        m + total_intermediate_bits + total_output_bits
    )  # Group 1 test bits: inputs, intermediate bits, and outputs
    num_group_2 = (
        total_carry_bits + total_aux_bits
    )  # Group 2 test bits: carry bits and auxiliary bits

    for values in product([0, 1], repeat=num_group_1):
        x_assigned = values[:m]

        intermediate_bits_assigned = values[m : m + total_intermediate_bits]
        y_assigned = values[m + total_intermediate_bits :]

        # compute the expected values of the intermediate, output bits, carry bits and auxiliary bits
        intermediate_bits_dict = (
            {}
        )  # helper data structure for tracking which intermediate bits are for which node
        for node_name, node_data in precomputed_tree.items():
            if (
                "n_intermediate_bits" in node_data
            ):  # if this node is at an intermediate layer
                if node_data["ind_level"] == 1:  # if this node is at level 1

                    # compute the intermediate sum value
                    if (
                        len(node_data["parent_list"]) == 2
                    ):  # if this node has two parents
                        index_1 = nonzero_indices.index(
                            int(node_data["parent_list"][0][1:]),
                        )
                        index_2 = nonzero_indices.index(
                            int(node_data["parent_list"][1][1:]),
                        )
                        intermediate_sum_value = (
                            a_[index_1] * x_assigned[index_1]
                            + a_[index_2] * x_assigned[index_2]
                        )
                    if (
                        len(node_data["parent_list"]) == 1
                    ):  # if this node has only one parent
                        index = nonzero_indices.index(
                            int(node_data["parent_list"][0][1:]),
                        )
                        intermediate_sum_value = a_[index] * x_assigned[index]

                else:  # if this node is at a level deeper than 1, which means that it uses IntegerAdder to add two intermediate values

                    # compute the intermediate value
                    parent0_name = node_data["parent_list"][0]
                    parent1_name = node_data["parent_list"][1]
                    parent0_int_value = sum(
                        [
                            2**i * intermediate_bits_dict[parent0_name][i]
                            for i in range(
                                0,
                                miadder.tree[parent0_name]["n_intermediate_bits"],
                            )
                        ],
                    )
                    parent1_int_value = sum(
                        [
                            2**i * intermediate_bits_dict[parent1_name][i]
                            for i in range(
                                0,
                                miadder.tree[parent1_name]["n_intermediate_bits"],
                            )
                        ],
                    )
                    intermediate_sum_value = parent0_int_value + parent1_int_value

                # store the value in the dictionary
                intermediate_bits_expected_at_this_node = [
                    int(bit)
                    for bit in bin(intermediate_sum_value)[2:].zfill(
                        node_data["n_intermediate_bits"],
                    )[::-1]
                ]
                intermediate_bits_dict[
                    node_name
                ] = intermediate_bits_expected_at_this_node

        intermediate_bits_expected = []
        for int_bits_value in intermediate_bits_dict.values():
            intermediate_bits_expected.extend(int_bits_value)

        # check if the intermediate bits and the output bits are correct
        assert (len(intermediate_bits_expected)) == total_intermediate_bits
        intermediate_bits_correct = all(
            [
                intermediate_bits_assigned[i] == intermediate_bits_expected[i]
                for i in range(0, total_intermediate_bits)
            ],
        )
        y_value_int = sum([2**i * y_assigned[i] for i in range(total_output_bits)])
        y_bits_correct = sum([a_[i] * x_assigned[i] for i in range(m)]) == y_value_int

        for additional_values in product([0, 1], repeat=num_group_2):
            carry_bits_assigned = additional_values[:total_carry_bits]
            aux_bits_assigned = additional_values[total_carry_bits:]

            # create a dictionary for the assignment
            assignment = {
                x_vars[i]: x_assigned[nonzero_indices.index(i)] for i in nonzero_indices
            }
            assignment.update(
                {
                    intermediate_bits[i]: intermediate_bits_assigned[i]
                    for i in range(0, total_intermediate_bits)
                },
            )
            assignment.update(
                {y_vars[i]: y_assigned[i] for i in range(0, total_output_bits)},
            )
            assignment.update(
                {
                    carry_bits[i]: carry_bits_assigned[i]
                    for i in range(total_carry_bits)
                },
            )
            assignment.update(
                {aux_bits[i]: aux_bits_assigned[i] for i in range(total_aux_bits)},
            )

            # check if the assigned carry and aux bits are correct by checking the number of equations satisfied
            # ^There is no need to check the values of the assigned carry and aux bits against their expected values because it is already checked in the
            # tests for each gadget (WeightIntegerAdder and IntegerAdder).
            eq_evals = miadder.evaluate(assignment)
            total_satisfied = sum(eq_evals)
            max_satisfied = (
                miadder.max_num_satisfied
            )  # maximum number of equations satisfied

            if total_satisfied == max_satisfied:
                print(
                    "   ".join(map(str, x_assigned + y_assigned))
                    + f"     {str(total_satisfied)}"
                    + f"   {str(intermediate_bits_correct)}"
                    + f"   {str(y_bits_correct)}",
                )
                correct_assignments.remove(list(x_assigned + y_assigned))

    # Check if all correct assignments are discovered as maximally satisfying solutions.
    if len(correct_assignments) == 0:
        if verbose:
            print("-----------------------------------------------------")
            print("All correct assignments are discovered.")
            print("-----------------------------------------------------")
            print("")
        return True
    else:
        if verbose:
            print("-----------------------------------------------------")
            print(
                "Not all correct assignments are covered by the set of maximally satisfying solutions.",
            )
            print("-----------------------------------------------------")
            print("")
        return False


